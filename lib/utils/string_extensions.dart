  17. 최소 8자리 이상 숫자, 특수문자가 각각 1개 이상 (패스워드 체크시 활용): /^(?=.*?[0-9])(?=.*?[#?!@$ %^&*-]).{8,}$/
  18. URL 체크 (프로토콜 포함, ex) http://www.naver.com ): /https?:\/\/(www\.)?[-a-zA-Z0-9@:%._\+~#=]{2,256}\.[a-z]{2,6}\b([-a-zA-Z0-9@:%_\+.~#()?&//=]*)/
  19. URL 체크 (프로토콜 옵션, ex) http://www.naver.com, www.naver.com)  : /(https?:\/\/)?(www\.)?[-a-zA-Z0-9@:%._\+~#=]{2,256}\.[a-z]{2,6}\b([-a-zA-Z0-9@:%_\+.~#?&//=]*)/
  20. HTML 태그인지 체크: /^<([a-z]+)([^<]+)*(?:>(.*)<\/\1>|\s+\/>)$/
  21. 하이퍼링크 체크: /(?!<a\sname=\"([\w\s\d\-\.\#]+)\"><\/a>)<a\sname=\"([\w\s\d\-\.\#]+)\">(.*?)<\/a>/
  22. 숫자인지 체크: ^(\(?\+?[0-9]*\)?)?[0-9_\- \(\)]*$
  23. 웹사이트 링크인지 체크: <a\s+(?:[^>]*)href=\"((?:https:\/\/|http:\/\/)(?:.*?))">(?:.*?)<\/a>
  24. 년월일 (yyyy-mm-dd) 체크: /^(19|20)\d\d([- /.])(0[1-9]|1[012])\2(0[1-9]|[12][0-9]|3[01])$/
  25. 주민등록번호 체크: /^[-A-Za-z0-9_]+[-A-Za-z0-9_.]*[@]{1}[-A-Za-z0-9_]+[-A-Za-z0-9_.]*[.]{1}[A-Za-z]{1,5}$/
   */

}