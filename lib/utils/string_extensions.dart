  6. 19자리 숫자와 "-"로 구성된 신용카드 번호: /^[0-9-]{19}$/
  7. 숫자로 구성된 4자리-4자리-4자리-4자리 유형 체크 (ex. 신용카드 번호): /^[0-9]{4}[-\s\.]?[0-9]{4}[-\s\.]?[0-9]{4}[-\s\.]?[0-9]{4}$/
